#!/usr/bin/env node
// inc/gen-collector.js
//
// Reads config/devices.json and regenerates collector/collector.yaml.
// Run with: node inc/gen-collector.js   (or: make gen-config)
//
// The collector filter uses OTTL "filter" processor logic:
//   Drop a metric if its name is NOT in the "collect: true" list.
//   i.e. if (name != "a" and name != "b" ...) → drop it.

const fs   = require('fs');
const path = require('path');

const ROOT   = path.join(__dirname, '..');
const CONFIG = JSON.parse(fs.readFileSync(path.join(ROOT, 'config', 'devices.json'), 'utf8'));

// ── Build the filter expression ───────────────────────────────────────────────
// Collect all metric names where collect === true across all devices.
const allowed = [];
for (const device of CONFIG.devices) {
  for (const metric of device.metrics) {
    if (metric.collect) allowed.push(metric.name);
  }
}

if (allowed.length === 0) {
  console.error('ERROR: No metrics have "collect": true — nothing would pass the filter.');
  process.exit(1);
}

// OTTL expression: drop a data point whose name is not in the allowed set.
// "name != A and name != B and ..." → metric is dropped if ALL conditions are true
const filterExpr = allowed.map(n => `name != "${n}"`).join(' and ');

// ── Build the YAML string ─────────────────────────────────────────────────────
// Written manually so we keep all the educational comments intact.
const yaml = `# collector/collector.yaml
#
# AUTO-GENERATED by inc/gen-collector.js — DO NOT EDIT BY HAND.
# Edit config/devices.json then run: make gen-config
#
# Allowed metrics (collect: true in devices.json):
${allowed.map(n => `#   - ${n}`).join('\n')}

receivers:
  otlp:
    protocols:
      grpc:
        endpoint: 0.0.0.0:4317   # Device(s) send here (gRPC)
      http:
        endpoint: 0.0.0.0:4318   # HTTP alternative

processors:
  # --- FILTER: drop any metric NOT in the allowed list ---
  # Generated from devices where "collect": true in config/devices.json
  filter/allowed_metrics:
    error_mode: ignore
    metrics:
      metric:
        - '${filterExpr}'

  # --- LABELS: stamp every metric with pipeline metadata ---
  attributes/add_env:
    actions:
      - key: environment
        value: production
        action: insert
      - key: pipeline
        value: otel-mock-demo
        action: insert

  # --- BATCH: group metrics before sending ---
  batch:
    timeout: 2s

exporters:
  # Send filtered metrics to the Node.js backend
  otlphttp/backend:
    endpoint: "http://localhost:4320"
    encoding: json
    tls:
      insecure: true

  # Prometheus scrape endpoint — http://localhost:9464/metrics
  prometheus:
    endpoint: "0.0.0.0:9464"

  # Print to collector logs (useful for debugging)
  debug:
    verbosity: detailed

service:
  pipelines:
    metrics:
      receivers:  [otlp]
      processors: [filter/allowed_metrics, attributes/add_env, batch]
      exporters:  [otlphttp/backend, prometheus, debug]
`;

const outPath = path.join(ROOT, 'collector', 'collector.yaml');
fs.writeFileSync(outPath, yaml, 'utf8');
console.log(`✓ collector/collector.yaml regenerated`);
console.log(`  Allowed metrics (${allowed.length}): ${allowed.join(', ')}`);
